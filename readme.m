## Image Requirements

- **Format**: JPG or PNG
- **Recommended size**: 
  - Background images: 1920x1080px or larger
  - Product images: 800x800px or larger
- **Optimization**: Compress images for web to ensure fast loading

## Placeholder Images

The website uses blur placeholders while images load. You can replace the placeholder base64 data in the components with your actual image blur data URLs for better UX.
